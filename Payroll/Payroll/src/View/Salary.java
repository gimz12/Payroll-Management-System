    private void txt_empidKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_empidKeyReleased
        ResultSet rss;
        try{
            int empid =Integer.parseInt(txt_empid.getText());
            CSalaryCheck CSC=new CSalaryCheck();
            rss =CSC.salaryCheck(empid);
        
            if(rss.next()){
                txt_otr.setText(rss.getString(4));
                txt_bonus.setText(rss.getString(5));
                txt_other.setText(rss.getString(6));
                txt_deduction.setText(rss.getString(8));
                txt_deductionres.setText(rss.getString(9));
                
                
            }
            else{
                txt_otr.setText("0");
                txt_bonus.setText("0");
                txt_other.setText("0");
                txt_deduction.setText("0");
                txt_deductionres.setText("");
            }
            try{
                empid =Integer.parseInt(txt_empid.getText());
                CEmployeeSearch CES=new CEmployeeSearch();
                rs=CES.searchEmployee(empid);
                rs.next();
                txt_basicsal.setText(rs.getString(13));
                txt_fname.setText(rs.getString(2));
                txt_sname.setText(rs.getString(3));
                txt_dep.setText(rs.getString(11));
                
                float bsal=Float.parseFloat(txt_basicsal.getText());
                float nprate=bsal/30;
                lbl_nprate.setText(String.valueOf(nprate));
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, "Invalid Employee Id Please Enter a Correct Employee ID");
            }
            try{
                empid =Integer.parseInt(txt_empid.getText());
                CAttendanceCheck CAC=new CAttendanceCheck();
                ResultSet rsa=CAC.attendaceCheck(empid);
                rsa.next();
                txt_othours.setText(rsa.getString(3));
                txt_npdays.setText(rsa.getString(4));
         
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, "Employee Does not Have a Attendance Record Please Add Attendance Record First");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
      
        
        
    }//GEN-LAST:event_txt_empidKeyReleased

