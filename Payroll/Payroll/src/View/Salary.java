    private void btn_calActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_calActionPerformed
    
        int empid=Integer.parseInt(txt_empid.getText());
        
        float overtime_rate=Float.parseFloat(txt_otr.getText());
        float oth=Float.parseFloat(txt_othours.getText());
        float bonus=Float.parseFloat(txt_bonus.getText());
        float other=Float.parseFloat(txt_other.getText());
        float deduction=Float.parseFloat(txt_deduction.getText());
        float nopaydays=Float.parseFloat(txt_npdays.getText());
        float nopayrate=Float.parseFloat(lbl_nprate.getText());
        float bsal=Float.parseFloat(txt_basicsal.getText());
        
        float totalnopay=nopaydays*nopayrate;
        float otallo=oth*overtime_rate;
        
        
        float totalallow=otallo+bonus+other;
        lbl_totalallow.setText(String.valueOf(totalallow));
        float totaldeduc=totalnopay+deduction;
        lbl_totaldeduc.setText(String.valueOf(totaldeduc));
        
        float grosssal=bsal+totalallow;
        lbl_grosssal.setText(String.valueOf(grosssal));
        float netsal=grosssal-totaldeduc;
        lbl_netsal.setText(String.valueOf(netsal));
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_btn_calActionPerformed

